        void LogExit(object? returnValue, [CallerMemberName] string methodName = "",
            [CallerArgumentExpression("returnValue")] string returnExpr = "");

        void LogExit([CallerMemberName] string methodName = "");

        // --- Sampled ---
        void LogSampled(string key, string message, int sampleRate = 25, [CallerMemberName] string methodName = "");

        // --- Debug ---
