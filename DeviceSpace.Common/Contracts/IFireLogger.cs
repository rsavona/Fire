    public interface IFireLogger<out TCategoryName> : IFireLogger
    {
        // Inherits all fast Serilog methods from the base interface
    }

    public partial interface IFireLogger
    {
        Serilog.ILogger GetRawLogger();

        FireLogger WithContext(string propertyName, object value);

        // --- Lifecycle ---
        void LogEnter(object? args = null, [CallerMemberName] string methodName = "",
